class TextBuilder:
    def __init__(self, lines, indent):
        self.lines = lines
        self.indent = indent

def emit_program(program):
    builder = builder_new()
    builder = builder_emit_line(builder, "// Generated by Sailfin self-hosted emitter")
    builder = builder_emit_blank(builder)
    builder = builder_emit_line( builder, "import() from \"sailfin/runtime\";" )
    builder = builder_emit_blank(builder)
    index = 0
    while True:
        if index >= len(program.statements):
            break
        builder = emit_statement(builder, program.statements[index])
        if index + 1 < len(program.statements):
            builder = builder_emit_blank(builder)
        index += 1
    return builder_to_string(builder)

def emit_statement(builder, statement):
    if statement.variant == "ImportDeclaration":
        return emit_import(builder, statement.items, statement.source)
    if statement.variant == "VariableDeclaration":
        return emit_variable(builder, statement)
    if statement.variant == "FunctionDeclaration":
        return emit_function(builder, statement.signature, statement.body, statement.decorators)
    if statement.variant == "StructDeclaration":
        return emit_struct(builder, statement)
    if statement.variant == "PipelineDeclaration":
        return emit_callable(builder, "pipeline", statement.signature, statement.body, statement.decorators)
    if statement.variant == "ToolDeclaration":
        return emit_callable(builder, "tool", statement.signature, statement.body, statement.decorators)
    if statement.variant == "TestDeclaration":
        return emit_test(builder, statement)
    if statement.variant == "ModelDeclaration":
        return emit_model(builder, statement)
    if statement.variant == "TypeAliasDeclaration":
        return emit_type_alias(builder, statement)
    if statement.variant == "InterfaceDeclaration":
        return emit_interface(builder, statement)
    if statement.variant == "EnumDeclaration":
        return emit_enum(builder, statement)
    if statement.variant == "Unknown":
        comment = "// original: " + collapse_whitespace(statement.text)
        return builder_emit_line(builder, comment)
    return builder_emit_line(builder, "// TODO: unsupported statement: " + statement.variant)

def emit_import(builder, items, source):
    list = join_with_separator(items, ", ")
    line = "import() from \"" + source + "\";"
    return builder_emit_line(builder, line)

def emit_variable(builder, statement):
    prefix = "let "
    if statement.mutable:
        prefix = prefix + "mut "
    prefix = prefix + statement.name
    prefix = prefix + format_type_annotation(statement.type_annotation)
    prefix = prefix + format_initializer(statement.initializer)
    return builder_emit_line(builder, prefix + ";")

def emit_function(builder, signature, body, decorators):
    current = emit_decorators(builder, decorators)
    header = format_function_header(signature)
    current = builder_emit_line(current, header)
    current = emit_block(current, body)
    return current

def emit_callable(builder, keyword, signature, body, decorators):
    current = emit_decorators(builder, decorators)
    header = format_callable_header(keyword, signature)
    current = builder_emit_line(current, header)
    current = emit_block(current, body)
    return current

def emit_test(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    name = format_test_name(statement.name)
    effects = format_effects(statement.effects)
    header = "test " + name
    if len(effects) > 0:
        header = header + " " + effects
    current = builder_emit_line(current, header)
    current = emit_block(current, statement.body)
    return current

def emit_model(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    header = "model " + statement.name + " : " + statement.model_type.text
    effects = format_effects(statement.effects)
    if len(effects) > 0:
        header = header + " " + effects
    current = builder_emit_line(current, header)
    current = builder_emit_line(current, "{")
    current = builder_push_indent(current)
    index = 0
    while True:
        if index >= len(statement.properties):
            break
        property = statement.properties[index]
        line = property.name + " = " + format_expression(property.value) + ";"
        current = builder_emit_line(current, line)
        index += 1
    if len(statement.properties) == 0:
        current = builder_emit_line(current, "// empty model body")
    current = builder_pop_indent(current)
    current = builder_emit_line(current, "}")
    return current

def emit_type_alias(builder, statement):
    line = "type " + statement.name
    line = line + format_type_parameters(statement.type_parameters)
    line = line + " = " + statement.aliased_type.text + ";"
    return emit_decorators_then_line(builder, statement.decorators, line)

def emit_interface(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    header = "interface " + statement.name
    header = header + format_type_parameters(statement.type_parameters)
    current = builder_emit_line(current, header)
    current = emit_block_start(current)
    index = 0
    while True:
        if index >= len(statement.members):
            break
        signature = statement.members[index]
        line = format_signature_line("fn", signature) + ";"
        current = builder_emit_line(current, line)
        index += 1
    current = emit_block_end(current)
    return current

def emit_enum(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    header = "enum " + statement.name
    header = header + format_type_parameters(statement.type_parameters)
    current = builder_emit_line(current, header)
    current = emit_block_start(current)
    index = 0
    while True:
        if index >= len(statement.variants):
            break
        variant = statement.variants[index]
        current = builder_emit_line(current, format_enum_variant(variant) + ";")
        index += 1
    current = emit_block_end(current)
    return current

def emit_struct(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    header = "struct " + statement.name
    header = header + format_type_parameters(statement.type_parameters)
    if len(statement.implements_types) > 0:
        header = header + " implements " + join_type_annotations(statement.implements_types)
    current = builder_emit_line(current, header)
    current = emit_block_start(current)
    field_index = 0
    while True:
        if field_index >= len(statement.fields):
            break
        current = builder_emit_line(current, format_field(statement.fields[field_index]) + ";")
        field_index += 1
    method_index = 0
    while True:
        if method_index >= len(statement.methods):
            break
        method = statement.methods[method_index]
        current = emit_decorators(current, method.decorators)
        current = builder_emit_line(current, format_method_header(method.signature))
        current = emit_block(current, method.body)
        if method_index + 1 < len(statement.methods):
            current = builder_emit_blank(current)
        method_index += 1
    if len(statement.fields) == 0  and  len(statement.methods) == 0:
        current = builder_emit_line(current, "// empty struct")
    current = emit_block_end(current)
    return current

def emit_block(builder, block):
    current = emit_block_start(builder)
    current = emit_block_body(current, block)
    current = emit_block_end(current)
    return current

def emit_block_body(builder, block):
    if len(block.statements) == 0:
        trimmed = trim_block_body(block.text)
        if len(trimmed) > 0:
            return builder_emit_line(builder, trimmed)
        return builder_emit_line(builder, "// empty body")
    current = builder
    index = 0
    while True:
        if index >= len(block.statements):
            break
        current = emit_block_statement(current, block.statements[index])
        index += 1
    return current

def emit_block_statement(builder, statement):
    if statement.variant == "ReturnStatement":
        if statement.expression == null:
            return builder_emit_line(builder, "return;")
        return builder_emit_line(builder, "return " + format_expression(statement.expression) + ";")
    if statement.variant == "ExpressionStatement":
        return builder_emit_line(builder, format_expression(statement.expression) + ";")
    if statement.variant == "VariableDeclaration":
        return emit_variable(builder, statement)
    if statement.variant == "PromptStatement":
        return emit_prompt(builder, statement)
    if statement.variant == "WithStatement":
        return emit_with(builder, statement)
    if statement.variant == "IfStatement":
        return emit_if(builder, statement)
    if statement.variant == "ForStatement":
        return emit_for(builder, statement)
    if statement.variant == "TestDeclaration":
        return emit_test(builder, statement)
    if statement.variant == "Unknown":
        line = "// original: " + collapse_whitespace(statement.text)
        return builder_emit_line(builder, line)
    if statement.variant == "FunctionDeclaration":
        return emit_function(builder, statement.signature, statement.body, statement.decorators)
    if statement.variant == "MatchStatement":
        return emit_match(builder, statement)
    return builder_emit_line(builder, "// TODO: unsupported block statement: " + statement.variant)

def emit_prompt(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    header = "prompt " + statement.channel
    current = builder_emit_line(current, header)
    current = emit_block(current, statement.body)
    return builder_emit_line(current, ";")

def emit_with(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    line = "with "
    index = 0
    while True:
        if index >= len(statement.clauses):
            break
        if index > 0:
            line = line + ", "
        line = line + format_expression(statement.clauses[index].expression)
        index += 1
    current = builder_emit_line(current, line)
    current = emit_block(current, statement.body)
    return current

def emit_if(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    line = "if " + format_expression(statement.condition)
    current = builder_emit_line(current, line)
    current = emit_block(current, statement.then_block)
    if statement.else_branch != null:
        current = emit_else_branch(current, statement.else_branch)
    return current

def emit_else_branch(builder, branch):
    if branch.body != null:
        current = builder_emit_line(builder, "else")
        return emit_block(current, branch.body)
    if branch.statement != null:
        if branch.statement.variant == "IfStatement":
            current = builder_emit_line(builder, "else if " + format_expression(branch.statement.condition))
            after = emit_block(current, branch.statement.then_block)
            if branch.statement.else_branch != null:
                return emit_else_branch(after, branch.statement.else_branch)
            return after
        current = builder_emit_line(builder, "else")
        return emit_block_statement(current, branch.statement)
    return builder

def emit_for(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    header = "for " + format_for_clause(statement.clause)
    current = builder_emit_line(current, header)
    return emit_block(current, statement.body)

def emit_match(builder, statement):
    current = emit_decorators(builder, statement.decorators)
    header = "match " + format_expression(statement.expression)
    current = builder_emit_line(current, header)
    current = builder_emit_line(current, "{")
    current = builder_push_indent(current)
    index = 0
    while True:
        if index >= len(statement.cases):
            break
        current = emit_match_case(current, statement.cases[index])
        index += 1
    current = builder_pop_indent(current)
    return builder_emit_line(current, "}")

def emit_match_case(builder, case):
    line = "case " + format_expression(case.pattern)
    if case.guard != null:
        line = line + " if " + format_expression(case.guard)
    line = line + " => {"
    current = builder_emit_line(builder, line)
    current = builder_push_indent(current)
    current = emit_block_body(current, case.body)
    current = builder_pop_indent(current)
    return builder_emit_line(current, "}")

def emit_block_start(builder):
    current = builder_emit_line(builder, "{")
    return builder_push_indent(current)

def emit_block_end(builder):
    current = builder_pop_indent(builder)
    return builder_emit_line(current, "}")

def emit_decorators_then_line(builder, decorators, line):
    current = emit_decorators(builder, decorators)
    return builder_emit_line(current, line)

def emit_decorators(builder, decorators):
    current = builder
    index = 0
    while True:
        if index >= len(decorators):
            break
        current = builder_emit_line(current, format_decorator(decorators[index]))
        index += 1
    return current

def format_decorator(decorator):
    line = "@" + decorator.name
    if len(decorator.arguments) == 0:
        return line
    parts = []
    index = 0
    while True:
        if index >= len(decorator.arguments):
            break
        parts = append_string(parts, format_decorator_argument(decorator.arguments[index]))
        index += 1
    return line + "(" + join_with_separator(parts, ", ") + ")"

def format_decorator_argument(argument):
    value = format_expression(argument.expression)
    if argument.name == null:
        return value
    return argument.name + ": " + value

def format_for_clause(clause):
    target = format_expression(clause.target)
    iterable = format_expression(clause.iterable)
    if len(target) == 0  or  len(iterable) == 0:
        return tokens_to_source(clause.tokens)
    return target + " in " + iterable

def format_function_header(signature):
    return format_signature_line("fn", signature)

def format_method_header(signature):
    return format_signature_line("fn", signature)

def format_callable_header(keyword, signature):
    return format_signature_line(keyword, signature)

def format_signature_line(keyword, signature):
    prefix = ""
    if signature.is_async:
        prefix = "async "
    line = prefix + keyword + " " + signature.name
    line = line + format_type_parameters(signature.type_parameters)
    line = line + "(" + format_parameters(signature.parameters) + ")"
    if signature.return_type != null:
        line = line + " -> " + signature.return_type.text
    effects = format_effects(signature.effects)
    if len(effects) > 0:
        line = line + " " + effects
    return line

def format_field(field):
    line = ""
    if field.mutable:
        line = line + "mut "
    line = line + field.name
    line = line + " -> " + field.type_annotation.text
    return line

def format_enum_variant(variant):
    if len(variant.fields) == 0:
        return variant.name
    parts = []
    index = 0
    while True:
        if index >= len(variant.fields):
            break
        parts = append_string(parts, format_field(variant.fields[index]))
        index += 1
    return variant.name + " { " + join_with_separator(parts, "; ") + " }"

def format_parameters(parameters):
    if len(parameters) == 0:
        return ""
    parts = []
    index = 0
    while True:
        if index >= len(parameters):
            break
        parts = append_string(parts, format_parameter(parameters[index]))
        index += 1
    return join_with_separator(parts, ", ")

def format_parameter(parameter):
    line = ""
    if parameter.mutable:
        line = "mut " + parameter.name
    else:
        line = parameter.name
    if parameter.type_annotation != null:
        line = line + " -> " + parameter.type_annotation.text
    if parameter.default_value != null:
        line = line + " = " + format_expression(parameter.default_value)
    return line

def format_type_parameters(parameters):
    if len(parameters) == 0:
        return ""
    names = []
    index = 0
    while True:
        if index >= len(parameters):
            break
        parameter = parameters[index]
        part = parameter.name
        if parameter.bound != null:
            part = part + " : " + parameter.bound.text
        names = append_string(names, part)
        index += 1
    return "<" + join_with_separator(names, ", ") + ">"

def format_type_annotation(annotation):
    if annotation == null:
        return ""
    return " -> " + annotation.text

def format_initializer(initializer):
    if initializer == null:
        return ""
    value = format_expression(initializer)
    if len(value) == 0:
        return ""
    return " = " + value

def format_effects(effects):
    if len(effects) == 0:
        return ""
    return "![" + join_with_separator(effects, ", ") + "]"

def join_type_annotations(values):
    if len(values) == 0:
        return ""
    parts = []
    index = 0
    while True:
        if index >= len(values):
            break
        parts = append_string(parts, values[index].text)
        index += 1
    return join_with_separator(parts, ", ")

def format_expression(expression):
    if expression.variant == "Identifier":
        return expression.name
    if expression.variant == "NumberLiteral":
        return expression.value
    if expression.variant == "BooleanLiteral":
        if expression.value:
            return "true"
        return "false"
    if expression.variant == "NullLiteral":
        return "null"
    if expression.variant == "StringLiteral":
        return quote_string(expression.value)
    if expression.variant == "Unary":
        operand = format_expression(expression.operand)
        if expression.operator == "!":
            return "!" + operand
        return expression.operator + operand
    if expression.variant == "Binary":
        left = format_expression(expression.left)
        right = format_expression(expression.right)
        op = expression.operator
        spaced = " " + op + " "
        return left + spaced + right
    if expression.variant == "Member":
        return format_expression(expression.object) + "." + expression.member
    if expression.variant == "Call":
        rendered = []
        index = 0
        while True:
            if index >= len(expression.arguments):
                break
            rendered = append_string(rendered, format_expression(expression.arguments[index]))
            index += 1
        args = join_with_separator(rendered, ", ")
        return format_expression(expression.callee) + "(" + args + ")"
    if expression.variant == "Index":
        target = format_expression(expression.sequence)
        offset = format_expression(expression.index)
        return target + "[" + offset + "]"
    if expression.variant == "Array":
        rendered = []
        index = 0
        while True:
            if index >= len(expression.elements):
                break
            rendered = append_string(rendered, format_expression(expression.elements[index]))
            index += 1
        contents = join_with_separator(rendered, ", ")
        return "[" + contents + "]"
    if expression.variant == "Object":
        rendered = []
        index = 0
        while True:
            if index >= len(expression.fields):
                break
            field = expression.fields[index]
            value = format_expression(field.value)
            rendered = append_string(rendered, field.name + ": " + value)
            index += 1
        body = join_with_separator(rendered, ", ")
        return "{" + " " + body + " " + "}"
    if expression.variant == "Struct":
        type_name = join_with_separator(expression.type_name, ".")
        rendered = []
        index = 0
        while True:
            if index >= len(expression.fields):
                break
            field = expression.fields[index]
            value = format_expression(field.value)
            rendered = append_string(rendered, field.name + ": " + value)
            index += 1
        body = join_with_separator(rendered, ", ")
        return type_name + " { " + body + " }"
    if expression.variant == "Range":
        start_value = format_expression(expression.start)
        end_value = format_expression(expression.end)
        return start_value + ".." + end_value
    if expression.variant == "Lambda":
        return format_lambda_expression(expression)
    if expression.variant == "Raw":
        return trim_text(expression.text)
    return ""

def format_lambda_expression(expression):
    params = format_lambda_parameters(expression.parameters)
    header = "fn " + params
    if expression.return_type != null:
        header = header + " -> " + expression.return_type.text
    body = format_lambda_body(expression.body)
    return header + " " + body

def format_lambda_parameters(parameters):
    rendered = []
    index = 0
    while True:
        if index >= len(parameters):
            break
        param = parameters[index]
        entry = param.name
        if param.type_annotation != null:
            entry = entry + " -> " + param.type_annotation.text
        rendered = append_string(rendered, entry)
        index += 1
    args = join_with_separator(rendered, ", ")
    return "(" + args + ")"

def format_lambda_body(body):
    lines = []
    if len(body.statements) == 0:
        lines = append_string(lines, "// empty body")
    else:
        index = 0
        while True:
            if index >= len(body.statements):
                break
            lines = append_string(lines, format_lambda_statement(body.statements[index]))
            index += 1
    indented = indent_lines(lines, 1)
    return "{\n" + join_with_separator(indented, "\n") + "\n}"

def format_lambda_statement(statement):
    if statement.variant == "ReturnStatement":
        if statement.expression == null:
            return "return;"
        return "return " + format_expression(statement.expression) + ";"
    if statement.variant == "ExpressionStatement":
        return format_expression(statement.expression) + ";"
    if statement.variant == "VariableDeclaration":
        line = "let "
        if statement.mutable:
            line = line + "mut "
        line = line + statement.name
        if statement.type_annotation != null:
            line = line + " -> " + statement.type_annotation.text
        if statement.initializer != null:
            line = line + " = " + format_expression(statement.initializer)
        return line + ";"
    if statement.variant == "Unknown":
        return "// original: " + collapse_whitespace(statement.text)
    return "// TODO: unsupported lambda statement: " + statement.variant

def indent_lines(lines, depth):
    result = []
    index = 0
    while True:
        if index >= len(lines):
            break
        prefix = ""
        count = 0
        while True:
            if count >= depth:
                break
            prefix = prefix + "    "
            count += 1
        result = append_string(result, prefix + lines[index])
        index += 1
    return result

def quote_string(value):
    result = "\""
    index = 0
    while True:
        if index >= len(value):
            break
        result = result + escape_string_char(value[index])
        index += 1
    result = result + "\""
    return result

def escape_string_char(ch):
    if ch == "\"":
        return "\\\""
    if ch == "\\":
        return "\\\\"
    if ch == "\n":
        return "\\n"
    if ch == "\r":
        return "\\r"
    if ch == "\t":
        return "\\t"
    return ch

def format_test_name(name):
    return quote_string(name)

def is_identifier(value):
    if len(value) == 0:
        return false
    first = value[0]
    if not is_identifier_start(first):
        return false
    index = 1
    while True:
        if index >= len(value):
            break
        if not is_identifier_part(value[index]):
            return false
        index += 1
    return true

def is_identifier_start(ch):
    if ch == "_":
        return true
    code = runtime.char_code(ch)
    return code >= runtime.char_code("a")  and  code <= runtime.char_code("z")  or  code >= runtime.char_code("A")  and  code <= runtime.char_code("Z")

def is_identifier_part(ch):
    if is_identifier_start(ch):
        return true
    code = runtime.char_code(ch)
    return code >= runtime.char_code("0")  and  code <= runtime.char_code("9")

def trim_block_body(text):
    trimmed = trim_text(text)
    if len(trimmed) == 0:
        return trimmed
    if trimmed[0] == "{"  and  trimmed[len(trimmed) - 1] == "}":
        return trim_text(runtime.substring(trimmed, 1, len(trimmed) - 1))
    return trimmed

def collapse_whitespace(value):
    result = ""
    index = 0
    last_space = false
    while True:
        if index >= len(value):
            break
        ch = value[index]
        is_space = ch == " "  or  ch == "\n"  or  ch == "\r"  or  ch == "\t"
        if is_space:
            if not last_space:
                result = result + " "
                last_space = true
        else:
            result = result + ch
            last_space = false
        index += 1
    return trim_text(result)

def tokens_to_source(tokens):
    if len(tokens) == 0:
        return ""
    parts = []
    index = 0
    while True:
        if index >= len(tokens):
            break
        parts = append_string(parts, tokens[index].lexeme)
        index += 1
    return collapse_whitespace(join_with_separator(parts, ""))

def builder_new():
    return TextBuilder(lines=[], indent=0)

def builder_emit_line(builder, line):
    prefix = ""
    count = 0
    while True:
        if count >= builder.indent:
            break
        prefix = prefix + "    "
        count += 1
    full_line = prefix + trim_right(line)
    lines = append_string(builder.lines, full_line)
    return TextBuilder(lines=lines, indent=builder.indent)

def builder_emit_blank(builder):
    lines = append_string(builder.lines, "")
    return TextBuilder(lines=lines, indent=builder.indent)

def builder_push_indent(builder):
    return TextBuilder(lines=builder.lines, indent=builder.indent + 1)

def builder_pop_indent(builder):
    indent = builder.indent
    if indent > 0:
        indent -= 1
    return TextBuilder(lines=builder.lines, indent=indent)

def builder_to_string(builder):
    content = join_with_separator(builder.lines, "\n")
    if len(content) == 0:
        return ""
    return content + "\n"

def trim_right(value):
    end = len(value)
    while True:
        if end <= 0:
            break
        ch = value[end - 1]
        if ch == " "  or  ch == "\t":
            end -= 1
            continue
        break
    if end == len(value):
        return value
    return runtime.substring(value, 0, end)

def append_string(values, value):
    return (values) + ([value])

def join_with_separator(values, separator):
    if len(values) == 0:
        return ""
    result = values[0]
    index = 1
    while True:
        if index >= len(values):
            break
        result = result + separator + values[index]
        index += 1
    return result

def trim_text(value):
    start = 0
    end = len(value)
    while True:
        if start >= end:
            break
        ch = value[start]
        if is_trim_char(ch):
            start += 1
            continue
        break
    while True:
        if end <= start:
            break
        ch = value[end - 1]
        if is_trim_char(ch):
            end -= 1
            continue
        break
    if start == 0  and  end == len(value):
        return value
    return runtime.substring(value, start, end)

def is_trim_char(ch):
    return ch == " "  or  ch == "\n"  or  ch == "\r"  or  ch == "\t"
